	
sel 
	trim(DATABASENAME) DATABASENAME,
	trim(TABLENAME) TABLENAME,
	sum(currentperm)/(1024*1024*1024) ACTUALSPACE_GB,
	(max(CURRENTPERM)*(HASHAMP()+1))/(1024*1024*1024) as EFFECTIVESPACE_GB
from
	DBC.TABLESIZE
where 1=1
	and trim(upper(DATABASENAME)) like '%EDWACC_TA4%'
	and trim(upper(TABLENAME))    like '%INT_SURVY_DKFO_DST_DTL%'
group by 1,2;


SELECT 
DATABASENAME,
CAST(SUM(MAXPERM)/(1024*1024*1024) AS DECIMAL(7,2)) MAX_PERM,
CAST(SUM(CURRENTPERM)/(1024*1024*1024) AS DECIMAL(7,2)) CURRENT_PERM,
CAST(SUM(MAXSPOOL)/(1024*1024*1024) AS DECIMAL(7,2)) MAX_SPOOL,
CAST(SUM(CURRENTSPOOL)/(1024*1024*1024) AS DECIMAL(7,2)) CURRENT_SPOOL 
FROM 
DBC.DISKSPACE 
WHERE DATABASENAME = 'YOURDATABASE' 
GROUP BY 
DATABASENAME ORDER BY MAX_PERM DESC;